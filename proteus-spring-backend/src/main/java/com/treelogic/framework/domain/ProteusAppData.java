package com.treelogic.framework.domain;



public class ProteusAppData {
	
	private int coilId;
	private int momentsCounter;
	private double currentX;
	private long counter;
	

	public int getCoilId() {
		return coilId;
	}

	public void setCoilId(int coilId) {
		this.coilId = coilId;
	}

	public int getMomentsCounter() {
		return momentsCounter;
	}

	public void setMomentsCounter(int momentsCounter) {
		this.momentsCounter = momentsCounter;
	}

	public double getCurrentX() {
		return currentX;
	}

	public void setCurrentX(double currentX) {
		this.currentX = currentX;
	}

	public long getCounter() {
		return counter;
	}

	public void setCounter(long counter) {
		this.counter = counter;
	}
	
	
	

}
